export const getProductos = (id, nombre, fabrica, marca, modelo) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve(filtrarProductosSiHayFiltros(id, nombre, fabrica, marca, modelo));
    }, 1000);
  });
};

export function filtrarProductosSiHayFiltros(
  id,
  nombre,
  fabrica,
  marca,
  modelo
) {
  let listaDeProductoFiltrados =
    id !== undefined
      ? listaDeProductos.filter((unProducto) =>
          unProducto.id
            .toString()
            .toUpperCase()
            .includes(id.toString().toUpperCase())
        )
      : listaDeProductos;
  listaDeProductoFiltrados =
    nombre !== undefined
      ? listaDeProductoFiltrados.filter((unProducto) =>
          unProducto.nombre.toString().includes(nombre.toString().toUpperCase())
        )
      : listaDeProductoFiltrados;
  listaDeProductoFiltrados =
    fabrica !== undefined
      ? listaDeProductoFiltrados.filter((unProducto) =>
          unProducto.codigoFabrica
            .toString()
            .toUpperCase()
            .includes(fabrica.toString().toUpperCase())
        )
      : listaDeProductoFiltrados;
  listaDeProductoFiltrados =
    marca !== undefined
      ? listaDeProductoFiltrados.filter((unProducto) =>
          unProducto.marca
            .toString()
            .toUpperCase()
            .includes(marca.toString().toUpperCase())
        )
      : listaDeProductoFiltrados;
  listaDeProductoFiltrados =
    modelo !== undefined
      ? listaDeProductoFiltrados.filter((unProducto) =>
          unProducto.modelos
            .toString()
            .toUpperCase()
            .includes(modelo.toString().toUpperCase())
        )
      : listaDeProductoFiltrados;
  return listaDeProductoFiltrados;
}

export const getProductoById = (idProducto) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve(
        listaDeProductos.find((unProducto) => unProducto.id === idProducto)
      );
    }, 2000);
  });
};

export const getUltimosProductos = () => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve(listaDeProductos.slice(listaDeProductos.length - 8));
    }, 2000);
  });
};
